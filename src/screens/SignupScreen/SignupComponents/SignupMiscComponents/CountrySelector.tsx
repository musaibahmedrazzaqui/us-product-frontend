import { Tooltip, TooltipProps } from '@rneui/themed';
import React from 'react'
import {
    TouchableOpacity,
    Text,
    ScrollView,
    View,
    Dimensions,
    FlatList
} from 'react-native'
import {Entypo} from '@expo/vector-icons'
import Flag from 'react-native-flags';

    const getISOCode = (country : string) =>{
        if (!country){
            return
        }
        return countryList.filter((item)=> item.name.toLowerCase() === country.toLocaleLowerCase())[0]
        
    }

  const ControlledTooltip: React.FC<TooltipProps | any> = (props) => {
    const [open, setOpen] = React.useState(false);
    return (
      <Tooltip
        visible={open}
        onOpen={() => {
          (!props.disabled) && setOpen(true);
        }}
        onClose={() => {
          setOpen(false);
        }}
        {...props}
        
        withPointer={false}
        skipAndroidStatusBar={false}
        popover={<ToolTipComponent list ={props.list} setSelectedStatus={(value : string)=>{props.setSelectedStatus(value); setOpen(false)}}/>}
      />
    );
  };

  function ToolTipComponent({setSelectedStatus, list} : {setSelectedStatus : any, list : any[]}) {
    // const MaritalStatuses = ['Islam', 'Hinduism', 'Christianity','Sikhism','Buddhism','Other']

    const ItemComponent = ({item} : any) =>{
        // console.log(name,code)
        return (
            <TouchableOpacity onPress={()=>setSelectedStatus(item.name)} style={{height:60, justifyContent:'flex-start', alignItems:'center', borderBottomColor: '#DFE0EB', borderBottomWidth:1,padding:10, flexDirection:'row'}}>
                <Flag code={item.code} size={24} type ="flat"/>
                <Text style={{fontFamily:'ArialNova', color:'black', paddingLeft:10}}>
                    {item.name}
                </Text>
            </TouchableOpacity>
        )
    }
    return (
        <FlatList data={countryList} renderItem={ItemComponent} style={{width:'100%', overflow:'hidden'}} showsVerticalScrollIndicator={false} keyExtractor={item=>item.code}/>
        // <ScrollView horizontal={false}>
        //     {countryList.map((item) => <ItemComponent item={item} key ={item.code}/>)}
        // </ScrollView>
    )
}

export default function CountrySelector({selectedValue, setValue, disableChange} : {selectedValue : any, setValue : any, disableChange? : any}){
    return (

            <ControlledTooltip
            disabled={disableChange}
            width={Dimensions.get('window').width * 0.85 }
            height={countryList.length > 5 ? Dimensions.get('window').height * 0.4 : countryList.length * 60}
            backgroundColor={'#fff'}
            closeOnlyOnBackdropPress={false}
            setSelectedStatus = {setValue}
            skipAndroidStatusBar={false}
            list = {countryList}
            containerStyle={{
                padding:0,
                // overflow:'hidden',
                shadowColor: "#000",
                shadowOffset: {
                    width: 0,
                    height: 2,
                },
                shadowOpacity: 0.06,
                shadowRadius: 3.84,
                // borderRadius:6,
                elevation: 5,
            }}
            pointerStyle = {{
                padding:0,
                // overflow:'hidden',
                shadowColor: "#000",
                shadowOffset: {
                    width: 0,
                    height: 2,
                },
                shadowOpacity: 0.06,
                shadowRadius: 3.84,

                elevation: 5,
            }}
            // highlightColor={'#ccc'}
          >
            

            <View
            style={{
                height:64,
                width:Dimensions.get('window').width * 0.9,
                borderWidth:1,
                borderColor:'#ddd',
                borderRadius:6,
                marginTop:10,
                justifyContent:'space-between',
                alignItems:'center',
                padding:10,
                flexDirection:'row'
            }}>
                <View style={{flexDirection:'row', alignItems:'center'}}>
                {(selectedValue != '' && selectedValue) && <Flag code={getISOCode(selectedValue).code} size={24} type ="flat"/>}
                <Text style={{paddingLeft:10}}>
                    {selectedValue === '' ? 'Select' : selectedValue}
                </Text>
                </View>
                <Entypo name='chevron-down' size={20}/>
            </View>
            </ControlledTooltip>
    )
}

